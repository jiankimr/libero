# ─── Parsing ─────────────────────────────────────────────────────────
def identify_file(filename: str):
    """Extract (dimension_key, noise_scale) from a comparison txt filename."""
    for suffix, dim_key in SUFFIX_TO_DIM.items():
        if suffix in filename:
            m = re.search(r"noise_(\d+)", filename)
            if m:
                return dim_key, int(m.group(1)) / 10000.0
    return None, None


def parse_comparison_file(filepath: pathlib.Path) -> dict:
    """Parse one comparison txt file → dict of metrics."""
    text = filepath.read_text(errors="replace")
    result = {}

    # ── Success rates ──
    m_b = re.search(r"Before \(No Noise\):\s+([\d.]+)%\s+\((\d+)/(\d+)", text)
    m_a = re.search(r"After \(With Noise\):\s+([\d.]+)%\s+\((\d+)/(\d+)", text)
    if m_b:
        result["success_before"] = float(m_b.group(1))
    if m_a:
        result["success_after"] = float(m_a.group(1))

    # ── Energy deltas ──
    m_draw = re.search(
        r"energy_draw\.sum\s+\|.*?\|\s+Δ\s+([+\-]?[\d.]+)%", text
    )
    m_regen = re.search(
        r"energy_regen\.sum\s+\|.*?\|\s+Δ\s+([+\-]?[\d.]+)%", text
    )
    m_net = re.search(
        r"energy_net\.sum\s+\|.*?\|\s+Δ\s+([+\-]?[\d.]+)%", text
    )
    if m_draw:
        result["energy_draw_sum_delta"] = float(m_draw.group(1))
    if m_regen:
        result["energy_regen_sum_delta"] = float(m_regen.group(1))
    if m_net:
        result["energy_net_sum_delta"] = float(m_net.group(1))

    # ── Life ratio – Rainflow (joint‑level averages) ──
    rf_sec = re.search(
        r"Rainflow - Joint별 분석:(.*?)(?:⚠️\s+Rainflow 가장)", text, re.DOTALL
    )
    if rf_sec:
        for m in re.finditer(
            r"Joint_(\d+)\s+\|\s+([\d.eE+\-]+)\s+\|\s+([\d.eE+\-]+)\s+\|\s+([\d.]+)x",
            rf_sec.group(1),
        ):
            j = int(m.group(1))
            result[f"rf_joint_{j}_ratio"] = float(m.group(4))

    # ── Life ratio – Fatpack (joint‑level averages) ──
    fp_sec = re.search(
        r"Fatpack - Joint별 분석:(.*?)(?:⚠️\s+Fatpack 가장)", text, re.DOTALL
    )
    if fp_sec:
        for m in re.finditer(
            r"Joint_(\d+)\s+\|\s+([\d.eE+\-]+)\s+\|\s+([\d.eE+\-]+)\s+\|\s+([\d.]+)x",
            fp_sec.group(1),
        ):
            j = int(m.group(1))
            result[f"fp_joint_{j}_ratio"] = float(m.group(4))

    return result


def collect_all_data() -> dict:
    """data[dim_key][scale] = {metrics…}"""
    from collections import defaultdict

    data = defaultdict(dict)
    for txt in sorted(RESULTS_DIR.glob("comparison_*par*.txt")):
        dim_key, scale = identify_file(txt.name)
        if dim_key is None:
            print(f"  SKIP: {txt.name}")
            continue
        metrics = parse_comparison_file(txt)
        data[dim_key][scale] = metrics
        print(f"  OK: {dim_key}  scale={scale}  ({len(metrics)} fields)")
    return data


# ─── Plot helpers ────────────────────────────────────────────────────
def _weakest_annotations(ax, scales, data_dim, method):
    """Annotate the weakest joint at each noise scale."""
    for s in scales:
        best_j, best_r = -1, 0
        for j in range(7):
            r = data_dim[s].get(f"{method}_joint_{j}_ratio", 0)
            if r > best_r:
                best_r = r
                best_j = j
        if best_j >= 0 and best_r > 1:
            ax.annotate(
                f"Joint {best_j}\n{best_r:,.1f}x",
                xy=(s, best_r),
                fontsize=7.5,
                textcoords="offset points",
                xytext=(0, 14),
                ha="center",
                fontweight="bold",
                bbox=dict(
                    boxstyle="round,pad=0.3",
                    facecolor="lightyellow",
                    edgecolor="gray",
                    alpha=0.85,
                ),
            )


def plot_life_ratio(data, dim_key, method="rf"):
    """Joint‑wise life ratio vs noise scale (log y)."""
    label = DIM_LABELS[dim_key]
    method_name = "Rainflow" if method == "rf" else "Fatpack"

    fig, ax = plt.subplots(figsize=(12, 7))
    scales = sorted(data[dim_key].keys())

    for j in range(7):
        xs, ys = [], []
        for s in scales:
            r = data[dim_key][s].get(f"{method}_joint_{j}_ratio")
            if r is not None:
                xs.append(s)
                ys.append(r)
        if ys:
            ax.plot(
                xs, ys, "o-",
                color=JOINT_COLORS[j],
                label=f"Joint {j}",
                linewidth=2,
                markersize=6,
            )

    _weakest_annotations(ax, scales, data[dim_key], method)

    ax.set_yscale("log")
    ax.axhline(y=1, color="gray", linestyle="--", alpha=0.5, linewidth=1)
    ax.set_xlabel(f"noise scale (raw)", fontsize=12)
    ax.set_ylabel("Life ratio (x, log scale)", fontsize=12)
    ax.set_title(
        f"Life Ratio vs Noise Scale (log)\n(Top: weakest joint & life ratio) — {method_name}",
        fontsize=14,
        fontweight="bold",
        color="#1a5276",
    )
    ax.set_xticks(scales)
    ax.set_xticklabels([str(s) for s in scales])
    ax.legend(loc="upper left", fontsize=9)
    ax.grid(True, alpha=0.3)
    fig.tight_layout()
    return fig


def plot_energy_change(data, dim_key):
    """Energy draw & regen delta% bar chart (all bars annotated)."""
    label = DIM_LABELS[dim_key]

    fig, ax = plt.subplots(figsize=(10, 6))
    scales = sorted(data[dim_key].keys())

    x_labels = ["0"] + [str(s) for s in scales]
    draw_d = [0.0] + [
        data[dim_key][s].get("energy_draw_sum_delta", 0.0) for s in scales
    ]
    regen_d = [0.0] + [
        data[dim_key][s].get("energy_regen_sum_delta", 0.0) for s in scales
    ]

    x = np.arange(len(x_labels))
    w = 0.35

    bars_draw = ax.bar(x - w / 2, draw_d, w,
                       label="draw_sum delta%", color="#DAA520", alpha=0.85)
    bars_regen = ax.bar(x + w / 2, regen_d, w,
                        label="regen_sum delta%", color="#5B9BD5", alpha=0.85)

    for bar, v in zip(bars_draw, draw_d):
        if v == 0:
            continue
        ax.text(bar.get_x() + bar.get_width() / 2,
                v + (1.0 if v >= 0 else -1.0),
                f"{v:+.1f}%", ha="center",
                va="bottom" if v >= 0 else "top",
                fontsize=7.5, fontweight="bold")

    for bar, v in zip(bars_regen, regen_d):
        if v == 0:
            continue
        ax.text(bar.get_x() + bar.get_width() / 2,
                v + (1.0 if v >= 0 else -1.0),
                f"{v:+.1f}%", ha="center",
                va="bottom" if v >= 0 else "top",
                fontsize=7.5, fontweight="bold")

    ax.axhline(y=0, color="#DAA520", linestyle="--", alpha=0.5)
    ax.set_xlabel("noise scale (raw)", fontsize=12)
    ax.set_ylabel("Change (%)", fontsize=12)
    ax.set_title(
        f"Energy Change (16-step sampling)\n{label}",
        fontsize=14, fontweight="bold",
    )
    ax.set_xticks(x)
    ax.set_xticklabels(x_labels)
    ax.legend(loc="upper left", fontsize=10)
    ax.grid(True, axis="y", alpha=0.3)
    fig.tight_layout()
    return fig


