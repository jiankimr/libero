def main():
    print("=" * 60)
    print(" Collecting data from comparison txt files ...")
    print("=" * 60)

    data = collect_all_data()

    print(f"\nDimensions found: {len(data)}")
    for dk in sorted(data.keys()):
        print(f"  {dk}: {sorted(data[dk].keys())}")

    all_dims = POSITION_DIMS + ROTATION_DIMS
    generated = []

    # ── Per‑dimension plots (24) ──
    for dim_key in all_dims:
        if dim_key not in data or not data[dim_key]:
            print(f"\n  SKIP {dim_key}: no data")
            continue

        dim_short = dim_key.split(".")[-1]
        cat = "position" if dim_key in POSITION_DIMS else "rotation"
        print(f"\n  Plotting {dim_key} …")

        for method in ("rf", "fp"):
            mname = "rainflow" if method == "rf" else "fatpack"
            fig = plot_life_ratio(data, dim_key, method=method)
            p = PLOTS_DIR / f"{cat}_life_ratio_{mname}_{dim_short}.png"
            fig.savefig(p, dpi=150); plt.close(fig)
            generated.append(p.name)

        fig = plot_energy_change(data, dim_key)
        p = PLOTS_DIR / f"{cat}_energy_change_{dim_short}.png"
        fig.savefig(p, dpi=150); plt.close(fig)
        generated.append(p.name)

        # energy_net in separate subfolder
        net_dir = PLOTS_DIR / "energy_net"
        net_dir.mkdir(parents=True, exist_ok=True)
        fig = plot_energy_net(data, dim_key)
        p = net_dir / f"{cat}_energy_net_{dim_short}.png"
        fig.savefig(p, dpi=150); plt.close(fig)
        generated.append(f"energy_net/{p.name}")

        fig = plot_success_rate(data, dim_key)
        p = PLOTS_DIR / f"{cat}_success_rate_{dim_short}.png"
        fig.savefig(p, dpi=150); plt.close(fig)
        generated.append(p.name)

    # ── Combined plots ──
    print("\n  Generating combined plots …")

    for cat, dim_keys, label in [
        ("position", POSITION_DIMS, "Position (x, y, z)"),
        ("rotation", ROTATION_DIMS, "Rotation (roll, pitch, yaw)"),
    ]:
        avail = [dk for dk in dim_keys if dk in data and data[dk]]
        if not avail:
            continue

        fig = plot_combined_success_rate(data, avail, label)
        p = PLOTS_DIR / f"{cat}_combined_success_rate.png"
        fig.savefig(p, dpi=150); plt.close(fig)
        generated.append(p.name)

        fig = plot_combined_energy(data, avail, label)
        p = PLOTS_DIR / f"{cat}_combined_energy.png"
        fig.savefig(p, dpi=150); plt.close(fig)
        generated.append(p.name)

        for method in ("rf", "fp"):
            mname = "rainflow" if method == "rf" else "fatpack"

            fig = plot_combined_life_ratio(data, avail, method, label)
            p = PLOTS_DIR / f"{cat}_combined_mean_life_ratio_{mname}.png"
            fig.savefig(p, dpi=150); plt.close(fig)
            generated.append(p.name)

            fig = plot_combined_weakest_joint(data, avail, method, label)
            p = PLOTS_DIR / f"{cat}_combined_weakest_joint_{mname}.png"
            fig.savefig(p, dpi=150); plt.close(fig)
            generated.append(p.name)

    # Position vs Rotation overview
    fig = plot_position_vs_rotation_summary(data)
    p = PLOTS_DIR / "overview_position_vs_rotation.png"
    fig.savefig(p, dpi=150, bbox_inches="tight"); plt.close(fig)
    generated.append(p.name)

    # ── Summary table to stdout ──
    print(f"\n{'=' * 80}")
    print(" DATA SUMMARY")
    print(f"{'=' * 80}")
    for dk in all_dims:
        if dk not in data:
            continue
        scales = sorted(data[dk].keys())
        print(f"\n  {DIM_LABELS[dk]}")
        print(f"  {'Scale':>6s}  {'SR%':>6s}  {'Draw%':>7s}  {'Regen%':>7s}"
              f"  {'RF mean':>8s}  {'FP mean':>8s}  {'RF worst':>10s}  {'FP worst':>10s}")
        print(f"  {'-'*72}")
        for s in scales:
            d = data[dk][s]
            sr = d.get("success_after", np.nan)
            draw = d.get("energy_draw_sum_delta", np.nan)
            regen = d.get("energy_regen_sum_delta", np.nan)
            rf_m = _mean_life_ratio(d, "rf")
            fp_m = _mean_life_ratio(d, "fp")
            wj_rf, wr_rf = _max_life_ratio(d, "rf")
            wj_fp, wr_fp = _max_life_ratio(d, "fp")
            print(f"  {s:6.2f}  {sr:5.1f}%  {draw:+6.1f}%  {regen:+6.1f}%"
                  f"  {rf_m:7.1f}x  {fp_m:7.1f}x"
                  f"  J{wj_rf}={wr_rf:7.1f}x  J{wj_fp}={wr_fp:7.1f}x")

    print(f"\n{'=' * 60}")
    print(f" Generated {len(generated)} plots in {PLOTS_DIR}/")
    for name in generated:
        print(f"   {name}")
    print("=" * 60)


if __name__ == "__main__":
    main()
