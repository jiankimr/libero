def plot_position_vs_rotation_summary(data):
    """Side-by-side bar chart: position mean vs rotation mean at each shared scale."""
    fig, axes = plt.subplots(1, 3, figsize=(18, 6))

    # Compute averages across dims for each category
    pos_scales = sorted(data[POSITION_DIMS[0]].keys())
    rot_scales = sorted(data[ROTATION_DIMS[0]].keys())

    def _avg_across_dims(dim_keys, metric_key):
        scales = sorted(data[dim_keys[0]].keys())
        out = []
        for s in scales:
            vals = [data[dk][s].get(metric_key, np.nan) for dk in dim_keys
                    if s in data[dk]]
            out.append(np.nanmean(vals))
        return scales, out

    # Panel 1: Success Rate
    ax = axes[0]
    ps, pv = _avg_across_dims(POSITION_DIMS, "success_after")
    rs, rv = _avg_across_dims(ROTATION_DIMS, "success_after")
