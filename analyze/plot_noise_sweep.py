    ax.axhline(y=baseline, color="red", linestyle="--", alpha=0.5,
               label=f"Baseline: {baseline:.1f}%")
    ax.set_xticks(range(max(len(ps), len(rs))))
    ax.set_xticklabels([f"s{i+1}" for i in range(max(len(ps), len(rs)))])
    ax.set_xlabel("Scale index (1–6)")
    ax.set_ylabel("Success Rate (%)")
    ax.set_title("Success Rate", fontweight="bold")
    ax.set_ylim(0, 100)
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3)

    # Panel 2: Energy Draw
    ax = axes[1]
    ps, pv = _avg_across_dims(POSITION_DIMS, "energy_draw_sum_delta")
    rs, rv = _avg_across_dims(ROTATION_DIMS, "energy_draw_sum_delta")
